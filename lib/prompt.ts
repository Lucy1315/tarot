export function buildReadingPrompt(input: ReadingInput): string {
  const cardList = input.cards
    .map(
      (c) =>
        `${c.position}: ${c.name}${c.reversed ? " 역방향" : ""}`
    )
    .join("\n");

  const questionSection = input.question
    ? `질문: "${input.question}"
이 질문의 맥락에 맞춰, 카드들이 만들어내는 전체 흐름을 읽어주세요.`
    : `특별한 질문 없이 전체적인 에너지와 흐름을 읽어주세요.`;

  return `${input.spreadName} 스프레드

${cardList}

${questionSection}

카드 뜻을 따로따로 나열하지 말고, 카드 간 연결과 전체 흐름 중심으로 해석해주세요. 실제 타로 상담처럼 자연스러운 대화체로 읽어주세요.`;
}
